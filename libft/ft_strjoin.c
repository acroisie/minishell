#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	int		i;
	int		j;
	char	*str;

	i = 0;
	j = 0;
	if (!s1)
		return (ft_strdup((char *)s2));
	if (!s2)
		return (ft_strdup((char *)s1));
	str = malloc((ft_strlen(s1) + ft_strlen(s2) + 1) * sizeof(char));
	if (str == NULL)
		return (NULL);
	while (s1[j])
		str[i++] = s1[j++];
	j = 0;
	while (s2[j])
		str[i++] = s2[j++];
	str[i] = '\0';
	return (str);
}

char	**ft_addline(char **src1, char *s2)
{
	int		i;
	int		j;
	char	**dest;

	i = 0;
	j = 0;
	dest = malloc((ft_destlen(src1) + 1) * sizeof(char *));
	if (dest == NULL)
		return (NULL);
	while (src1[j])
		dest[i++] = src1[j++];
	j = 0;
	dest[i] = ft_strdup(s2);
	dest[++i] = NULL;
	free(s2);
	return (dest);
}

